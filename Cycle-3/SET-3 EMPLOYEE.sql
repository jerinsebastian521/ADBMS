SELECT E.ENAME, D.DNAME FROM EMPLOYEE E LEFT OUTER JOIN DEPARTMENT D ON E.DNO=D.DNUMBER ORDER BY D.DNAME ASC, E.ENAME ASC; 

SELECT P.PNUMBER, P.PNAME,COUNT(E.SSN) AS EMPLOYEES FROM WORKIN W LEFT OUTER JOIN PROJECT P ON W.PNO=P.PNUMBER LEFT OUTER JOIN EMPLOYEE E ON W.ESSN=E.SSN GROUP BY P.PNAME, P.PNUMBER;

SELECT P.PNUMBER, P.PNAME,COUNT(E.SSN) AS EMPLOYEES  FROM WORKIN W LEFT OUTER JOIN PROJECT P ON W.PNO=P.PNUMBER LEFT OUTER JOIN EMPLOYEE E ON W.ESSN=E.SSN 
GROUP BY P.PNAME, P.PNUMBER HAVING COUNT(E.SSN)>2;

SELECT P.PNUMBER, P.PNAME, D.DNUMBER, COUNT(E.SSN)  FROM PROJECT P LEFT OUTER JOIN DEPARTMENT D on D.DNUMBER=P.DNUMBER LEFT OUTER JOIN EMPLOYEE E on E.DNO=P.DNUMBER 
GROUP BY P.PNAME, P.PNUMBER, D.DNUMBER HAVING D.DNUMBER=5;

