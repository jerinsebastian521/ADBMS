CREATE TABLE CUSTOMER
(
CID INT,
CNAME VARCHAR(20) NOT NULL,
LOC VARCHAR(20),
SEX VARCHAR(20),
DOJ DATE,
PRIMARY KEY(CID)
);

CREATE TABLE BANK_BRN
(
BCODE INT,
BLOC VARCHAR(20),
BSTATE VARCHAR(20),
PRIMARY KEY(BCODE)
);

CREATE TABLE DEPOSIT
(
DACNO INT,
DTYPE VARCHAR(20),
DDATE DATE,
DAMT INT,
PRIMARY KEY(DACNO)
);

CREATE TABLE LOAN
(
LACNO INT,
LTYPE VARCHAR(20),
LDATE DATE,
LAMT INT,
PRIMARY KEY(LACNO)
);

CREATE TABLE ACCOUNT_IN
(
BCODE INT,
CID INT,
FOREIGN KEY(BCODE) REFERENCES BANK_BRN(BCODE),
FOREIGN KEY(CID) REFERENCES CUSTOMER(CID )
);

CREATE TABLE DEPOSITOR
(
DACNO INT,
CID INT,
FOREIGN KEY(DACNO) REFERENCES DEPOSIT(DACNO ),
FOREIGN KEY(CID) REFERENCES CUSTOMER(CID )
);

CREATE TABLE BORROWER
(
LACNO INT,
CID INT,
FOREIGN KEY(LACNO) REFERENCES LOAN(LACNO ),
FOREIGN KEY(CID) REFERENCES CUSTOMER(CID )
);


